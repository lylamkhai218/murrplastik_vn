let currentLang = localStorage.getItem('mp_lang') || 'vi';

function t(key) {
  return (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key]) || key;
}

function applyTranslations() {
  // Translate document title if it has data-i18n
  const titleEl = document.querySelector('title');
  if (titleEl && titleEl.getAttribute('data-i18n')) {
    document.title = t(titleEl.getAttribute('data-i18n'));
  }
  
  // Translate meta description if it has data-i18n
  const metaDescEl = document.querySelector('meta[name="description"]');
  if (metaDescEl && metaDescEl.getAttribute('data-i18n')) {
    metaDescEl.setAttribute('content', t(metaDescEl.getAttribute('data-i18n')));
  }

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const attr = el.getAttribute('data-i18n-attr');
    if (attr) {
      el.setAttribute(attr, t(key));
    } else {
      el.textContent = t(key);
    }
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.getAttribute('data-i18n-html'));
  });
  document.documentElement.lang = currentLang;
  // Update active lang button
  document.querySelectorAll('.lang-btn button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
  // Update alternate link
  const altLink = document.querySelector('link[rel="alternate"]');
  if (altLink) altLink.setAttribute('hreflang', currentLang === 'vi' ? 'en' : 'vi');
}

function switchLang(lang) {
  if (lang === currentLang) return;
  currentLang = lang;
  localStorage.setItem('mp_lang', lang);
  applyTranslations();
}

document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
  document.querySelectorAll('.lang-btn button').forEach(btn => {
    btn.addEventListener('click', () => switchLang(btn.dataset.lang));
  });
});
